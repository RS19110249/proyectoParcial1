    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if(jtProductos.getSelectedRow()>= 0){
            int idProducto = (int) jtProductos.getValueAt(jtProductos.getSelectedRow(), 0);
            frmNProducto obj = new frmNProducto(idProducto);
            obj.setTitle("Modificar producto");
            obj.setModal(true);
            obj.setVisible(true);
            jtProductos.setModel(new Producto().GetAllModel());
        }else{
            JOptionPane.showMessageDialog(rootPane, "Se debe seleccionar un registro");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(jtProductos.getSelectedRow()>= 0){
            int idProducto = (int) jtProductos.getValueAt(jtProductos.getSelectedRow(), 0);
            Producto obj = new Producto();
            obj.setidProducto(idProducto);
            if (obj.Delete()) {
                JOptionPane.showMessageDialog(null, "Producto eliminado", "OPERACIÓN COMPLETADA",
                JOptionPane.INFORMATION_MESSAGE);
            }
            jtProductos.setModel(new Producto().GetAllModel());
        }else{
            JOptionPane.showMessageDialog(rootPane, "Se debe seleccionar un registro", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

